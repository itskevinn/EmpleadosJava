    private void guardarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBotonActionPerformed
nombre=nombreText.getText();
identificacion=identificacionText.getText();
tipoEmpleado=(String) tipoEmpleadoCombo.getSelectedItem();
salario=(colocarSalario(tipoEmpleado));
prima=calcularPrima(tipoEmpleado,salario);
if("CONTRATISTA".equals(tipoEmpleado)){
 empleadosCombo.addItem(nombre+"   "+tipoEmpleado+"     "+salario);
}
if("DOCENTE".equals(tipoEmpleado) || "ADMINISTRATIVO".equals(tipoEmpleado) || "CONTRATISTA".equals(tipoEmpleado)){
    listaEmpleados.add(new Empleado(nombre, identificacion,tipoEmpleado,prima,salario));
    conPrima++;
}
else{
    sinPrima++;
    JOptionPane.showMessageDialog(null, "Esta persona no tiene derecho a prima");
}
conMayorPrima();
conMenorPrima();
nombreText.setText(" ");
identificacionText.setText(" ");
tipoEmpleadoCombo.setSelectedIndex(0);
    }//GEN-LAST:event_guardarBotonActionPerformed

