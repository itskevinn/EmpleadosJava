public long totalAsignadoPrimas(){
    for (int i = 0; i < listaEmpleados.size(); i++) {
        totPrimas=totPrimas+listaEmpleados.get(i).getPrima();
    }
    return totPrimas;
}
public Empleado conMayorPrima(){ 
    for (int i = 0; i < listaEmpleados.size(); i++) {
        if(listaEmpleados.get(i).getPrima()>mayor){
        mayor=listaEmpleados.get(i).getPrima();
        conMayor=listaEmpleados.get(i);
    }
}
    return conMayor;
}
public Empleado conMenorPrima(){ 
    for (int i = 0; i < listaEmpleados.size(); i++) {
        if(listaEmpleados.get(i).getPrima()<menor){
        menor=listaEmpleados.get(i).getPrima();
        conMenor=listaEmpleados.get(i);
}
}
    return conMenor;
}
public long calcularPrima(String tipoEmpleado, long salario){
    long primaE=0;
    if("DOCENTE".equals(tipoEmpleado)){
        primaE=(long) (0.35*3700000);
    }
    if("ADMINISTRATIVO".equals(tipoEmpleado)){
        primaE=(long) (0.24*4000000);
    }
    if("CONTRATISTA".equals(tipoEmpleado)){
        primaE=(long) (0.12*3500000);
    }
    return primaE;
}
public long colocarSalario(String tipoEmpleado){
        long salarioE = 0;
    if("DOCENTE".equals(tipoEmpleado)){
        salarioE=3700000;
    }
    if("ADMINISTRATIVO".equals(tipoEmpleado)){
        salarioE=4000000;
    }
    if("CONTRATISTA".equals(tipoEmpleado)){
        salarioE=3500000;
    }
    return salarioE;
}
    
